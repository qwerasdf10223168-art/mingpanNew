def normalize_token(t: str) -> str:
    """å»åŒç¾©ã€å°¾ç¶´ï¼ˆå»Ÿæ—ºé™·å¹³ç¥¿æ¬Šç§‘å¿Œåˆ©ï¼‰ï¼Œä¸ç ã€æ˜Ÿã€å­—ã€‚"""
