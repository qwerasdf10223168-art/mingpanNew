    if not anchor_col:
        return [""] * len(cols)
    labels, out = PALACE_ORDER[:], [""] * len(cols)
    start_idx = cols.index(anchor_col)
    for offset in range(len(cols)):
        pos = (start_idx + offset) % len(cols)
        out[pos] = labels[offset % len(labels)]
    return out

